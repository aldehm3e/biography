function readText(file) {
  return readFileSync(path.join(root, file), "utf8").replace(/^\uFEFF/, "");
}

function writeText(file, content) {
  writeFileSync(path.join(root, file), content, "utf8");
}

function bundleComponents(name, files) {
  const content = [
    `/*! ${generatedHeader} Bundle: ${name}. */`,
    ...files.map((file) => {
      const sourcePath = path.join(ndsDir, file);
      const source = readFileSync(sourcePath, "utf8").replace(/^\uFEFF/, "");
      return `\n/*! ${file} */\n${source}\n;`;
    })
  ].join("\n");
  const bundlePath = path.join("js", `${name}.bundle.js`);
  const minPath = path.join("js", `${name}.bundle.min.js`);
  writeText(bundlePath, content);
  minifyJs(bundlePath, minPath);
}

function minifyJs(input, output) {
  const terserArgs = ["--yes", "terser", input, "--compress", "--mangle", "--comments", "/^!/", "--output", output];
  const command = process.platform === "win32" ? "cmd.exe" : "npx";
  const args = process.platform === "win32" ? ["/d", "/s", "/c", "npx", ...terserArgs] : terserArgs;
  execFileSync(command, args, {
    cwd: root,
    stdio: "inherit"
  });
}

function splitSelectorList(selectorText) {
  const selectors = [];
  let current = "";
  let parens = 0;
  let brackets = 0;
  let quote = "";

  for (let i = 0; i < selectorText.length; i++) {
    const ch = selectorText[i];
    const next = selectorText[i + 1];
    if (quote) {
      current += ch;
      if (ch === "\\" && next) {
        current += next;
        i++;
      } else if (ch === quote) {
        quote = "";
      }
      continue;
    }
    if (ch === "\"" || ch === "'") {
      quote = ch;
      current += ch;
      continue;
    }
    if (ch === "(") parens++;
    if (ch === ")") parens = Math.max(0, parens - 1);
    if (ch === "[") brackets++;
    if (ch === "]") brackets = Math.max(0, brackets - 1);
    if (ch === "," && parens === 0 && brackets === 0) {
      selectors.push(current.trim());
      current = "";
      continue;
    }
    current += ch;
  }
  if (current.trim()) selectors.push(current.trim());
  return selectors;
}

function isAdminSelector(selector) {
  const s = selector.trim();
  if (!s || s.startsWith("@")) return false;
  if (/body:not\(\[data-page=(?:"|')admin(?:"|')\]\)/.test(s)) return false;
  if (/body\[data-page=(?:"|')admin(?:"|')\]/.test(s)) return true;
  return [
    /\.admin-(?!persona|trigger)[a-z0-9_-]+/i,
    /\.editor-(?:list|item|accordion|drag|delete)/i,
    /\.compact-editor-[a-z0-9_-]+/i,
    /\.sortable-editor-[a-z0-9_-]+/i,
    /\.panel-heading\b/i,
    /\.form-grid\b/i,
    /\.contact-form-grid\b/i,
    /\.contact-editor-[a-z0-9_-]+/i,
    /\.contact-delete-(?:btn|icon)\b/i,
    /\.home-number-icon-[a-z0-9_-]+/i,
    /\.page-children-[a-z0-9_-]+/i,
    /\.page-editor-[a-z0-9_-]+/i,
    /\.card-(?:items|link-fields)/i,
    /\.footer-(?:nested|cookie)-[a-z0-9_-]+/i,
    /\.nested-editor-[a-z0-9_-]+/i,
    /\.media-library-[a-z0-9_-]+/i,
    /\.upload(?:able|-file)-[a-z0-9_-]+/i,
    /\.permission-check\b/i,
    /\.system-[a-z0-9_-]+/i,
    /\.integration-[a-z0-9_-]+/i,
    /\.page-feedback-admin-[a-z0-9_-]+/i,
    /\.saudi-map-region-(?:editor|row|name|value-input)\b/i,
    /\[data-admin-(?:dashboard|panel|tab|group|users|user|permission|settings|home|footer|navigation|system|refresh|add|save|delete)/i,
    /\[data-(?:home-section-panel|home-numbers-editor|saudi-map-regions-editor|saudi-map-metric-icon-editor|footer-section-panel|pages-editor|card-collections-editor|card-items-editor|projects-editor|integrations-editor|cookie-footer-pages-list|sortable-list)/i
  ].some((pattern) => pattern.test(s));
}

function isAdminRule(selectorText) {
  const selectors = splitSelectorList(selectorText);
  return selectors.length > 0 && selectors.every(isAdminSelector);
}

function findMatchingBrace(css, openIndex, end) {
  let depth = 1;
  let quote = "";
  for (let i = openIndex + 1; i < end; i++) {
    const ch = css[i];
    const next = css[i + 1];
    if (quote) {
      if (ch === "\\" && next) {
        i++;
      } else if (ch === quote) {
        quote = "";
      }
      continue;
    }
    if (ch === "/" && next === "*") {
      i += 2;
      while (i < end && !(css[i] === "*" && css[i + 1] === "/")) i++;
      i++;
      continue;
    }
    if (ch === "\"" || ch === "'") {
      quote = ch;
      continue;
    }
    if (ch === "{") depth++;
    if (ch === "}") {
      depth--;
      if (depth === 0) return i;
    }
  }
  throw new Error("Unable to find matching CSS brace.");
}

function findNextBrace(css, start, end) {
  let quote = "";
  for (let i = start; i < end; i++) {
    const ch = css[i];
    const next = css[i + 1];
    if (quote) {
      if (ch === "\\" && next) {
        i++;
      } else if (ch === quote) {
        quote = "";
      }
      continue;
    }
    if (ch === "/" && next === "*") {
      i += 2;
      while (i < end && !(css[i] === "*" && css[i + 1] === "/")) i++;
      i++;
      continue;
    }
    if (ch === "\"" || ch === "'") {
      quote = ch;
      continue;
    }
    if (ch === "{") return i;
  }
  return -1;
}

function splitCssRange(css, start = 0, end = css.length) {
  let publicCss = "";
  let adminCss = "";
  let cursor = start;

  while (cursor < end) {
    const open = findNextBrace(css, cursor, end);
    if (open === -1) {
      publicCss += css.slice(cursor, end);
      break;
    }
    const close = findMatchingBrace(css, open, end);
    const prelude = css.slice(cursor, open);
    const preludeTrimmed = prelude.trim();
    const block = css.slice(cursor, close + 1);

    if (/^@(media|supports|container|layer)\b/i.test(preludeTrimmed)) {
      const inner = splitCssRange(css, open + 1, close);
      if (inner.publicCss.trim()) publicCss += `${prelude}{${inner.publicCss}}`;
      if (inner.adminCss.trim()) adminCss += `${prelude}{${inner.adminCss}}`;
    } else if (!preludeTrimmed.startsWith("@") && isAdminRule(preludeTrimmed)) {
      adminCss += block;
    } else {
      publicCss += block;
    }
    cursor = close + 1;
  }

  return { publicCss, adminCss };
}

function generatedAdminBody(css) {
  return css.replace(/^\/\*![\s\S]*?\*\/\s*/u, "");
}

function splitCustomCss() {
  const customPath = path.join("css", "custom.css");
  const adminPath = path.join("css", "admin.css");
  const custom = readText(customPath);
  const existingAdmin = existsSync(path.join(root, adminPath)) ? readText(adminPath) : "";
  const source = existingAdmin.includes(generatedHeader)
    ? `${custom}\n\n${generatedAdminBody(existingAdmin)}`
    : custom;
  const split = splitCssRange(source);
  const adminCss = `/*! ${generatedHeader} Admin-only rules split from css/custom.css. */\n${split.adminCss.trim()}\n`;
  writeText(customPath, `${split.publicCss.trim()}\n`);
  writeText(adminPath, adminCss);
}

function updateHtml() {
  const pages = ["index.html", "projects.html", "project.html", "cards.html", "pages.html", "notifications.html", "admin.html"];
  for (const page of pages) {
    let html = readText(page);
    const isAdmin = page === "admin.html";
    html = html.replace(/css\/custom\.css\?v=[^"]+/g, `css/custom.css?v=${version}`);
    html = html.replace(/css\/admin\.css\?v=[^"]+/g, `css/admin.css?v=${version}`);
    if (isAdmin && !html.includes("css/admin.css")) {
      html = html.replace(
        /(<link rel="stylesheet" href="css\/custom\.css\?v=[^"]+">\r?\n)/,
        `$1  <link rel="stylesheet" href="css/admin.css?v=${version}">\n`
      );
    }
    const bundle = isAdmin ? "nds-admin.bundle.min.js" : "nds-public.bundle.min.js";
    html = html.replace(
      /(?:\r?\n\s*<script src="assets\/vendor\/nds\/components\/js\/[^"]+"><\/script>)+/u,
      `\n  <script src="js/${bundle}?v=${version}"></script>`
    );
    writeText(page, html);
  }
}

